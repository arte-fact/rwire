// HandlerSpec - Bridge between macros and runtime
// ============================================================================

/// Specification for a handler, determining its execution strategy.
///
/// The `#[handler]` macro generates this instead of `HandlerFn`. The storage
/// type of the state determines whether the handler requires a server round-trip.
#[derive(Clone)]
pub struct HandlerSpec {
    /// The storage type of the state this handler operates on.
    pub storage_type: StorageType,
    /// The TypeId of the state this handler operates on.
    pub state_type_id: Option<TypeId>,
    /// The remote handler function.
    pub remote_handler: Option<HandlerFn>,
    /// Parameter bytes to send with the event (for ItemRef-based handlers).
    /// These bytes are encoded when binding the event and sent back with the event.
    pub param_bytes: Option<Vec<u8>>,
    /// Set of fields that this handler modifies.
    /// Used for fine-grained reactivity - only renderers depending on these
    /// fields will re-render after this handler executes.
    pub changes: ChangeSet,
    /// Debounce delay in milliseconds (0 = no debounce).
    /// When > 0, uses BIND_DEBOUNCED opcode instead of BIND_REMOTE.
    pub debounce_ms: u16,
    /// Stable, content-derived handler identity (FNV-1a of the handler's
    /// module path + name, set by the `#[handler]` macro).
    ///
    /// Sent on the wire instead of a positional table index, so a binding maps
    /// to the same id across renders, restarts, and reconnects — the basis for
    /// reusing DOM nodes (morphing) without misrouting events. `0` = unset
    /// (handlers built without the macro); such handlers fall back to a
    /// content hash at registration time.
    pub handler_id: u32,
}

impl HandlerSpec {
    /// Create a HandlerSpec from a handler function and state type.
    ///
    /// This legacy method assumes all fields change (ChangeSet::all()).
    /// For fine-grained reactivity, use `from_fn_with_changes` instead.
    pub fn from_fn<S: State + Default + 'static>(handler: fn(&mut S)) -> Self {
        Self::from_fn_with_changes::<S>(handler, ChangeSet::all())
    }

    /// Create a HandlerSpec from a handler function with explicit ChangeSet.
    ///
    /// This is called by the `#[handler]` macro with auto-detected changes.
    pub fn from_fn_with_changes<S: State + Default + 'static>(
        handler: fn(&mut S),
        changes: ChangeSet,
    ) -> Self {
        Self {
            storage_type: S::STORAGE_TYPE,
            state_type_id: Some(TypeId::of::<S>()),
            remote_handler: Some(HandlerFn::new_state_with_changes::<S>(handler, changes)),
            param_bytes: None,
            changes,
            debounce_ms: 0,
            handler_id: 0,
        }
    }

    /// Create a HandlerSpec from a handler function with EventContext.
    ///
    /// This legacy method assumes all fields change (ChangeSet::all()).
    /// For fine-grained reactivity, use `from_fn_with_context_and_changes` instead.
    pub fn from_fn_with_context<S: State + Default + 'static>(
        handler: fn(&mut S, &EventContext),
    ) -> Self {
        Self::from_fn_with_context_and_changes::<S>(handler, ChangeSet::all())
    }

    /// Create a HandlerSpec from a handler function with EventContext and explicit ChangeSet.
    ///
    /// This is called by the `#[handler]` macro for handlers with EventContext parameter.
    pub fn from_fn_with_context_and_changes<S: State + Default + 'static>(
        handler: fn(&mut S, &EventContext),
        changes: ChangeSet,
    ) -> Self {
        Self {
            storage_type: S::STORAGE_TYPE,
            state_type_id: Some(TypeId::of::<S>()),
            remote_handler: Some(HandlerFn::new_with_context_and_changes::<S>(
                handler, changes,
            )),
            param_bytes: None,
            changes,
            debounce_ms: 0,
            handler_id: 0,
        }
    }

    /// Create a memory HandlerSpec with a remote handler.
    pub fn memory<S: MemoryState>(handler: fn(&mut S)) -> Self {
        Self {
            storage_type: StorageType::Memory,
            state_type_id: Some(TypeId::of::<S>()),
            remote_handler: Some(HandlerFn::new::<S>(handler)),
            param_bytes: None,
            changes: ChangeSet::all(),
            debounce_ms: 0,
            handler_id: 0,
        }
    }

    /// Get the ChangeSet for this handler.
    pub fn changes(&self) -> ChangeSet {
        self.changes
    }

    /// Add parameter bytes to this handler spec (returns a new spec).
    ///
    /// This is used by `ElementBuilder::on_ref()` to attach item reference
    /// data to the handler.
    pub fn with_param_bytes(mut self, bytes: Vec<u8>) -> Self {
        self.param_bytes = Some(bytes);
        self
    }

    /// Set debounce delay in milliseconds.
    ///
    /// The handler will only fire after `ms` milliseconds of inactivity.
    /// This is equivalent to using `on_debounced()` but as a builder method.
    ///
    /// # Example
    ///
    /// ```ignore
    /// el(El::Input).on(Ev::Input, search_handler().debounce(300))
    /// ```
    pub fn debounce(mut self, ms: u16) -> Self {
        self.debounce_ms = ms;
        self
    }

    /// Set the stable handler id (called by the `#[handler]` macro).
    pub fn with_handler_id(mut self, id: u32) -> Self {
        self.handler_id = id;
        self
    }

    /// The stable handler id, or `0` if it was not assigned by the macro.
    pub fn handler_id(&self) -> u32 {
        self.handler_id
    }

    /// Get the state type ID.
    pub fn state_type_id(&self) -> Option<TypeId> {
        self.state_type_id
    }

    /// Get the remote handler, if any.
    pub fn remote_handler(&self) -> Option<&HandlerFn> {
        self.remote_handler.as_ref()
    }
}

/// Largest value the 3-byte wire varint can carry (see `protocol::varint`).
///
/// Handler ids travel as varints in bind/event frames, so they must fit this
/// range — a wider value would be silently truncated on the wire and never
/// resolve back to its map entry.
pub const HANDLER_ID_MAX: u32 = 0x0040_0000 - 1; // 22-bit space, < VARINT_MAX

/// Compute a stable handler id from a handler's module path and name.
///
/// FNV-1a (32-bit) over `"<module>::<name>"`, folded into the wire-varint range
/// ([`HANDLER_ID_MAX`]). Deterministic across renders, process restarts, and
/// reconnects, so a binding resolves to the same id over time. Used by the
/// `#[handler]` macro via `module_path!()` + the fn name. Never returns `0`
/// (which is reserved for "unset").
#[must_use]
pub fn stable_handler_id(module: &str, name: &str) -> u32 {
    let mut h: u32 = 0x811c_9dc5;
    let mix = |h: &mut u32, b: u8| {
        *h ^= u32::from(b);
        *h = h.wrapping_mul(0x0100_0193);
    };
    for b in module.bytes() {
        mix(&mut h, b);
    }
    mix(&mut h, b':');
    mix(&mut h, b':');
    for b in name.bytes() {
        mix(&mut h, b);
    }
    let h = h & HANDLER_ID_MAX;
    if h == 0 { 1 } else { h }
}

impl std::fmt::Debug for HandlerSpec {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("HandlerSpec")
            .field("storage_type", &self.storage_type)
            .field("has_state_type_id", &self.state_type_id.is_some())
            .field("has_remote_handler", &self.remote_handler.is_some())
            .field("has_param_bytes", &self.param_bytes.is_some())
            .finish()
    }
}

/// Type alias for stateful handler functions.
pub type StatefulHandler<S> = fn(&mut S);

/// Type alias for renderer functions.
pub type Renderer<S> = fn(&S) -> ElementBuilder;

/// A type-erased handler that can mutate state.
pub struct HandlerFn {
    /// The type-erased handler function
    inner: Box<dyn HandlerInner>,
    /// Which fields this handler modifies (for fine-grained reactivity)
    changes: ChangeSet,
    /// Storage type for this handler's state
    storage_type: StorageType,
    /// Table name for persisted state (None for non-persisted)
    table_name: Option<&'static str>,
}

trait HandlerInner: Send + Sync {
    fn state_type_id(&self) -> TypeId;
    fn create_state(&self) -> Box<dyn Any + Send + Sync>;
    fn call(&self, state: &mut dyn Any);
    fn call_with_context(&self, state: &mut dyn Any, ctx: &EventContext);
    fn needs_context(&self) -> bool;
    fn clone_box(&self) -> Box<dyn HandlerInner>;
    /// Get a unique identifier for this handler (function pointer address)
    fn fn_id(&self) -> usize;
}

/// Handler that takes only state (legacy, single-parameter).
struct TypedHandler<S: Default + Send + Sync + 'static> {
    handler: fn(&mut S),
    _marker: std::marker::PhantomData<S>,
}

/// Handler that takes state and EventContext (new, two-parameter).
struct TypedHandlerWithContext<S: Default + Send + Sync + 'static> {
    handler: fn(&mut S, &EventContext),
    _marker: std::marker::PhantomData<S>,
}

impl<S: Default + Send + Sync + 'static> HandlerInner for TypedHandler<S> {
    fn state_type_id(&self) -> TypeId {
        TypeId::of::<S>()
    }

    fn create_state(&self) -> Box<dyn Any + Send + Sync> {
        Box::new(S::default())
    }

    fn call(&self, state: &mut dyn Any) {
        if let Some(s) = state.downcast_mut::<S>() {
            (self.handler)(s);
        }
    }

    fn call_with_context(&self, state: &mut dyn Any, _ctx: &EventContext) {
        // Legacy handler ignores context
        self.call(state);
    }

    fn needs_context(&self) -> bool {
        false
    }

    fn clone_box(&self) -> Box<dyn HandlerInner> {
        Box::new(TypedHandler {
            handler: self.handler,
            _marker: std::marker::PhantomData,
        })
    }

    fn fn_id(&self) -> usize {
        self.handler as usize
    }
}

impl<S: Default + Send + Sync + 'static> HandlerInner for TypedHandlerWithContext<S> {
    fn state_type_id(&self) -> TypeId {
        TypeId::of::<S>()
    }

    fn create_state(&self) -> Box<dyn Any + Send + Sync> {
        Box::new(S::default())
    }

    fn call(&self, state: &mut dyn Any) {
        // If called without context, use empty context
        self.call_with_context(state, &EventContext::empty());
    }

    fn call_with_context(&self, state: &mut dyn Any, ctx: &EventContext) {
        if let Some(s) = state.downcast_mut::<S>() {
            (self.handler)(s, ctx);
        }
    }

    fn needs_context(&self) -> bool {
        true
    }

    fn clone_box(&self) -> Box<dyn HandlerInner> {
        Box::new(TypedHandlerWithContext {
            handler: self.handler,
            _marker: std::marker::PhantomData,
        })
    }

    fn fn_id(&self) -> usize {
        self.handler as usize
    }
}

impl Clone for HandlerFn {
    fn clone(&self) -> Self {
        Self {
            inner: self.inner.clone_box(),
            changes: self.changes,
            storage_type: self.storage_type,
            table_name: self.table_name,
        }
    }
}

impl HandlerFn {
    /// Create a new handler for the given MemoryState type (legacy API).
    /// Uses `ChangeSet::all()` for backwards compatibility.
    pub fn new<S: MemoryState>(handler: fn(&mut S)) -> Self {
        Self {
            inner: Box::new(TypedHandler {
                handler,
                _marker: std::marker::PhantomData,
            }),
            changes: ChangeSet::all(),
            storage_type: StorageType::Memory,
            table_name: None,
        }
    }

    /// Create a new handler for the given State type.
    /// Uses `ChangeSet::all()` for backwards compatibility.
    pub fn new_state<S: State + Default>(handler: fn(&mut S)) -> Self {
        Self::new_state_with_changes::<S>(handler, ChangeSet::all())
    }

    /// Create a new handler for the given State type with explicit ChangeSet.
    pub fn new_state_with_changes<S: State + Default>(
        handler: fn(&mut S),
        changes: ChangeSet,
    ) -> Self {
        let storage_type = S::STORAGE_TYPE;
        let table_name = if S::TABLE_NAME.is_empty() {
            None
        } else {
            Some(S::TABLE_NAME)
        };
        Self {
            inner: Box::new(TypedHandler {
                handler,
                _marker: std::marker::PhantomData,
            }),
            changes,
            storage_type,
            table_name,
        }
    }

    /// Create a new handler with context support for the given State type.
    /// Uses `ChangeSet::all()` for backwards compatibility.
    pub fn new_with_context<S: State + Default>(handler: fn(&mut S, &EventContext)) -> Self {
        Self::new_with_context_and_changes::<S>(handler, ChangeSet::all())
    }

    /// Create a new handler with context support for the given State type with explicit ChangeSet.
    pub fn new_with_context_and_changes<S: State + Default>(
        handler: fn(&mut S, &EventContext),
        changes: ChangeSet,
    ) -> Self {
        let storage_type = S::STORAGE_TYPE;
        let table_name = if S::TABLE_NAME.is_empty() {
            None
        } else {
            Some(S::TABLE_NAME)
        };
        Self {
            inner: Box::new(TypedHandlerWithContext {
                handler,
                _marker: std::marker::PhantomData,
            }),
            changes,
            storage_type,
            table_name,
        }
    }

    /// Get the ChangeSet for this handler.
    pub fn changes(&self) -> ChangeSet {
        self.changes
    }

    /// Get the TypeId of the state this handler operates on.
    pub fn state_type_id(&self) -> TypeId {
        self.inner.state_type_id()
    }

    /// Create a new default state of the correct type.
    pub fn create_state(&self) -> Box<dyn Any + Send + Sync> {
        self.inner.create_state()
    }

    /// Call the handler with the given state (legacy, no context).
    pub fn call(&self, state: &mut dyn Any) {
        self.inner.call(state);
    }

    /// Call the handler with the given state and event context.
    pub fn call_with_context(&self, state: &mut dyn Any, ctx: &EventContext) {
        self.inner.call_with_context(state, ctx);
    }

    /// Check if this handler needs context (uses 2-parameter signature).
    pub fn needs_context(&self) -> bool {
        self.inner.needs_context()
    }

    /// Get a unique identifier for this handler (function pointer address).
    ///
    /// This is used to match handlers during synced element updates,
    /// where we need to find the correct handler index for rebinding events.
    pub fn fn_id(&self) -> usize {
        self.inner.fn_id()
    }

    /// Get the storage type for this handler's state.
    pub fn storage_type(&self) -> StorageType {
        self.storage_type
    }

    /// Get the table name for persisted state (None for non-persisted).
    pub fn table_name(&self) -> Option<&'static str> {
        self.table_name
    }
}

impl std::fmt::Debug for HandlerFn {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("HandlerFn")
            .field("state_type_id", &self.state_type_id())
            .finish()
    }
}

