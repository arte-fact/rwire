/// Renderer dependency tracking using compile-time bitmasks.
///
/// Field IDs (0-63) map to bit positions in a u64. This enables zero-cost
/// dependency checking at runtime using a single bitwise AND operation.
///
/// # Example
///
/// ```ignore
/// // Auto-generated by #[renderer] macro:
/// const DEPS: RendererDeps = RendererDeps::from_fields(&[Counter::FIELD_COUNT]);
/// ```
#[derive(Clone, Copy, Debug, Default, PartialEq, Eq)]
pub struct RendererDeps {
    /// Bitmask where bit i is set if renderer depends on field i
    pub mask: u64,
    /// If true, ignore mask and always re-render (fallback for complex cases)
    pub always: bool,
}

impl RendererDeps {
    /// Create deps that always re-render (fallback for dynamic dependencies).
    ///
    /// Use this for renderers that have complex or dynamic field access patterns
    /// that can't be statically analyzed.
    pub const fn always() -> Self {
        Self {
            mask: 0,
            always: true,
        }
    }

    /// Create deps from compile-time field IDs.
    ///
    /// Field IDs are generated by `#[derive(State)]` as `Type::FIELD_*` constants.
    /// Only fields 0-63 are supported; higher IDs are ignored.
    pub const fn from_fields(field_ids: &[u8]) -> Self {
        let mut mask = 0u64;
        let mut i = 0;
        while i < field_ids.len() {
            if field_ids[i] < 64 {
                mask |= 1u64 << field_ids[i];
            }
            i += 1;
        }
        Self {
            mask,
            always: false,
        }
    }

    /// Create deps with no dependencies (never re-renders automatically).
    pub const fn none() -> Self {
        Self {
            mask: 0,
            always: false,
        }
    }

    /// Check if this renderer needs update given changed fields.
    ///
    /// Returns true if:
    /// - `always` is set, OR
    /// - `changes.all_changed` is set, OR
    /// - There's overlap between dependency mask and change mask
    #[inline]
    pub const fn needs_update(&self, changes: ChangeSet) -> bool {
        self.always || changes.all_changed || (self.mask & changes.mask) != 0
    }

    /// Merge two RendererDeps (union of dependencies).
    pub const fn merge(self, other: Self) -> Self {
        Self {
            mask: self.mask | other.mask,
            always: self.always || other.always,
        }
    }
}

/// Tracks which fields changed during a handler execution.
///
/// Uses the same u64 bitmask as RendererDeps for zero-cost comparison.
/// This is generated by the `#[handler]` macro based on static analysis
/// of field mutations in the handler body.
///
/// # Example
///
/// ```ignore
/// // Auto-generated by #[handler] macro:
/// const CHANGES: ChangeSet = ChangeSet::from_fields(&[Counter::FIELD_COUNT]);
/// ```
#[derive(Clone, Copy, Debug, Default, PartialEq, Eq)]
pub struct ChangeSet {
    /// Bitmask where bit i is set if field i was modified
    pub mask: u64,
    /// If true, assume all fields changed (fallback for complex mutations)
    pub all_changed: bool,
}

impl ChangeSet {
    /// Create an empty changeset (no fields changed).
    pub const fn new() -> Self {
        Self {
            mask: 0,
            all_changed: false,
        }
    }

    /// Create a changeset indicating all fields changed.
    ///
    /// Use this as a fallback when mutations can't be statically determined,
    /// or for handlers that use methods like `push()`, `retain()`, etc.
    pub const fn all() -> Self {
        Self {
            mask: !0,
            all_changed: true,
        }
    }

    /// Create changeset from compile-time field IDs.
    ///
    /// Field IDs are generated by `#[derive(State)]` as `Type::FIELD_*` constants.
    pub const fn from_fields(field_ids: &[u8]) -> Self {
        let mut mask = 0u64;
        let mut i = 0;
        while i < field_ids.len() {
            if field_ids[i] < 64 {
                mask |= 1u64 << field_ids[i];
            }
            i += 1;
        }
        Self {
            mask,
            all_changed: false,
        }
    }

    /// Check if any field changed.
    pub const fn is_empty(&self) -> bool {
        !self.all_changed && self.mask == 0
    }

    /// Merge two changesets (union of changes).
    pub const fn merge(self, other: Self) -> Self {
        Self {
            mask: self.mask | other.mask,
            all_changed: self.all_changed || other.all_changed,
        }
    }

    /// Add a single field to the changeset.
    pub const fn with_field(mut self, field_id: u8) -> Self {
        if field_id < 64 {
            self.mask |= 1u64 << field_id;
        }
        self
    }
}

